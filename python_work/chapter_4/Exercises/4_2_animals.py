#make a list of pets
pets = ['dog', 'rabbit', 'cat', 'fish', 'hamster']

#printing without for loop
print(pets)

#printing with for loop
for pet in pets:
	print(pet)

#printing with additional statement
for pet in pets:
	print("A " + pet + " would make a great pet.")
print("All of these animals would make great pets!")

